  <div class="col-md-8">
	  <p align="justify">Makam Soekarno adalah kompleks pemakaman dari presiden pertama RI Indonesia yang sekaligus proklamator 
		  
		  kemerdekaan Indonesia, Soekarno. Makam ini merupakan makam dengan gaya arsitektur Jawa, dimana terdapat 
		  
		  Joglo yang menjadi ciri khas utamanya.
	</p>
	
	
	  <p align="justify">Makam Soekarno terletak di Kelurahan Bendogerit, Kecamatan Sanawetan Kota Blitar. Komplek makam ini 
		  
		  berdiri seluas 1,8 sejak Ir Soekarno wafat dan dimakamkan di sana. Pada tanggal 21 Juni 1970, kompleks 
		  
		  makam ini untuk pertama kalinya dipugar. Dengan pemugaran itu pencitraan Makam Bung Karno sebagai ikon 
		  
		  Kota Blitar semakin dikukuhkan. Ikon itulah yang mampu menyedot pengunjung berziarah di sana.
		  
	  </p>

	</div>
	
	<p align="justify">Sejak 2004, pengembangan kembali dilakukan dengan menambahkan bangunan baru yang menjadi satu kompleks 
		
		dengan makam Bung Karno tersebut, yaitu Perpustakaan dan Museum Bung Karno. Tim arsiteknya diketuai 
		
		oleh Pribadi Widodo dan Baskoro Tedjo dari Institut Teknologi Bandung.
	</p>

	<p align="justify">Pintu masuk Makam ini dimulai dari jalanan yang menghubungkan perpustakaan yang berada di sisi selatan 
		
		komplek makam hingga sampai pada gapura Agung yang menghadap ke selatan. Bangunan utama disebut dengan 
		
		Cungkup Makam Bung Karno. Cungkup ini berbentuk bangunan Joglo, dan diberi nama Astono Mulyo. Diatas 
		
		Makam diletakkan sebuah batu pualam hitam bertuliskan : "Disini dimakamkan Bung Karno Proklamator 
		
		Kemerdekaan Dan Presiden Pertama Republik Indonesia. Penyambung Lidah Rakyat Indonesia."
		
	</p>
	
	
	

