	<p align="justify">Candi Penataran, adalah sebuah candi berlatar belakang Hindu yang telah ada sejak kerajaan Kediri dan digunakan sampai era kerajaan Majapahit.
	</p>
	
	  <p align="justify">Komplek candi Penataran ini merupakan komplek candi terbesar di Jawa Timur dan terletak di lereng barat daya Gunung Kelud. Terletak pada ketinggian 450 M dari permukaan laut, komplek candi Penataran ini terletak di desa Panataran, kecamatan Nglegok, Blitar.
		  
	  </p>	  
  </div>
  
	<p align="justify">Candi Penataran ditemukan pada tahun 1815, dan belum banyak dikenal sampai tahun 1850. Komplek candi ini ditemukan oleh Sir Thomas Stamford Raffles, yang merupakan Letnan Gubernur Jendral pada masa kolonial Inggris di Indonesia pada waktu itu.
		
	</p>
	
	<p align="justify">Nama asli candi Penataran dipercaya adalah Candi Palah yang disebut dalam prasasti Palah, dan dibangun pada tahun 1194 oleh Raja Çrnga (Syrenggra) yang bergelar Sri Maharaja Sri Sarweqwara Triwikramawataranindita Çrengalancana Digwijayottungadewa. Raja Çrnga memerintah kerajaan Kediri antara tahun 1190 - 1200, sebagai candi gunung untuk tempat upacara pemujaan agar dapat menetralisasi atau menghindari mara bahaya yang disebabkan oleh gunung Kelud yang sering meletus.
		
	</p>
	
