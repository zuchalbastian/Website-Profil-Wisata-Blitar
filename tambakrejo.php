<h2>Pantai Tambakrejo</h2>
<br>

<div class="row">
  <div class="col-xs-9 col-md-7"><img src="img/IMG_0448.jpg" width="90%"></div>
  <div class="col-xs-8 col-md-5">
	<p align="justify">Pantai Tambakrejo terletak di desa tambakrejo kecamatan Wonotirto, kurang lebih 30km ke arah selatan dari kota Blitar.
	</p>
	
	  <p align="justify">Pantai Tambakrejo merupakan pantai yang banyak dikunjungi di kota Blitar, dengan pemandangan yang indah, pantai yang cukup bersih, air laut yang biru dan pasir putih yang terbentang menjadi sebuah teluk dengan panjang sekitar 10 Km. Ombak pantai Tambakrejo pun tidak terlalu besar, sehingga aman untuk bermain-main atau bahkan mandi di pantainya.
	  </p>	  
  </div>
  
	<p align="justify">Di Pintu masuk pantai kearah timur terdapat Kampung nelayan dengan perahu-perahu nelayan yang ditambatkan disampingnya dan Tempat Pelelangan Ikan untuk hasil nelayan yang baru melaut. Bila anda datang di pagi hari maka akan menjumpai ikan-ikan segar hasil tangkapan nelayan.
		
	</p>
</div>