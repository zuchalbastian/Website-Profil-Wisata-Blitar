	<p align="justify">Pantai Serang merupakan pantai yang terletak di pesisir Samudra Hindia, tepatnya berada di desa Serang, kecamatan Panggungrejo, kurang lebih 45 Km arah barat daya kota Blitar.
	</p>
	
	  <p align="justify">Sama seperti kebanyakan pantai di Blitar, pantai Serangpun menjadi pantai yang dipakai untuk ritual tradisional Larung Saji saat tanggal 1 Suro.
		  
	  </p>
	  
  </div>
	
		
	<p align="justify">Pantai Serang merupakan satu komplek pantai yang terdiri dari 3 kawasan. Pantai ini memiliki hamparan pasir putih yang landai dengan ombak yang tidak begitu deras. Dahulu di sekitar pantai ini dipenuhi oleh batu-batuan putih yang indah, warga sekitar menyebutnya dengan batu Lintang karena model batunya yang putih berkilauan.
		
	</p>
	
	<p align="justify">Sebenarnya ada 3 pantai dalam komplek kawasan wisata ini, selain pantai utama tempat diadakannya upacara larung saji, bila kita menelusuri jalan setapak yang melintasi bukit disebelah barat maka akan sampai pada pantai ke dua dengan pasir putih yang tidak terlalu luas bila dibandingkan dengan pantai yang pertama.
		
	</p>	
  
